$(function() {
 
  //画像にマウスを乗せたら発動
  $('.do3 .dl1').hover(function() {
 
    //画像のsrc属性が別画像のパスに切り替わる
    $('.do3 .dl1 dt img').attr('src', 'img/do4-2.png');
 
  //ここにはマウスを離したときの動作を記述
  }, function() {
 
    //画像のsrc属性を元の画像のパスに戻す
    $('.do3 .dl1 dt img').attr('src', 'img/do4-1.png');
 
  });
});
