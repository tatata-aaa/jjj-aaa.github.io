$(function() {
 
  //画像にマウスを乗せたら発動
  $('.eo4 .dl3').hover(function() {
 
    //画像のsrc属性が別画像のパスに切り替わる
    $('.eo4 .dl3 span').css('background', '#6c3a24');
 
  //ここにはマウスを離したときの動作を記述
  }, function() {
 
    //画像のsrc属性を元の画像のパスに戻す
    $('.eo4 .dl3 span').css('background', '#eb8400');
 
  });
});
